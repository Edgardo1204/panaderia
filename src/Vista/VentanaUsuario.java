    private void cContrasenaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cContrasenaKeyReleased
        // Contraseña de 8-16 caracteres con al menos un dígito, al menos uno
        // letra minúscula, al menos una letra mayúscula, al menos una
        // caracter especial sin espacios en blanco
        String PASSWORD_REGEX = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{8,12}$";

        Pattern PASSWORD_PATTERN = Pattern.compile(PASSWORD_REGEX);
        // Validar una contraseña
        if (PASSWORD_PATTERN.matcher(cContrasena.getText()).matches())
        {
            lblvalido.setForeground(Color.green);
            lblvalido.setText("Contraseña válida");
        } else
        {
            lblvalido.setForeground(Color.red);
            lblvalido.setText("No cumple con el formato");
        }
    }//GEN-LAST:event_cContrasenaKeyReleased

